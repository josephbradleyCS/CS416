public class LibraryCalendar {

    private int date;

    /**
     * This does stuff.
     */
    public LibraryCalendar() {
        this.date = 0;
    }

    /**
     * This does stuff.
     * @return System.out.println("");
     */
    public int getDate() {
        return date;
    }


    /**
     * This does stuff.
     */
    public void advance() {
        this.date++;
    }
}
